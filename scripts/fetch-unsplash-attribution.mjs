#!/usr/bin/env node
/**
 * Populate lib/unsplash/attribution-map.ts with photographer attribution
 * for every Unsplash CDN photo-id referenced by our structural templates
 * and admin seed data.
 *
 * Usage:
 *   UNSPLASH_ACCESS_KEY=... node scripts/fetch-unsplash-attribution.mjs
 *
 * The script:
 *   1. Scans lib/templates/structural-templates.ts and
 *      lib/templates/admin-seed-data.ts for `photo-XXXXX` slugs.
 *   2. Calls Unsplash's GET /photos/{id} endpoint for each unique slug.
 *   3. Rewrites lib/unsplash/attribution-map.ts with the populated entries.
 *
 * Re-running is safe and idempotent. New IDs added to templates after a
 * previous run will be fetched; removed IDs will be dropped from the map.
 *
 * Note on photo IDs: Unsplash's API accepts the CDN slug (e.g.
 * `photo-1495020689067-958852a7765e`) as a valid photo identifier. If a
 * lookup fails for a given slug, the script logs the failure and writes
 * `null` for that entry — the renderer treats `null` as "no caption"
 * rather than rendering an uncredited image.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');

const TEMPLATE_FILES = [
    path.join(repoRoot, 'lib/templates/structural-templates.ts'),
    path.join(repoRoot, 'lib/templates/admin-seed-data.ts'),
];

const MAP_FILE = path.join(repoRoot, 'lib/unsplash/attribution-map.ts');

const UTM_QUERY = 'utm_source=keystoneweb&utm_medium=referral';

function extractIds() {
    const ids = new Set();
    for (const file of TEMPLATE_FILES) {
        const text = readFileSync(file, 'utf8');
        const matches = text.matchAll(/photo-[a-zA-Z0-9_-]+/g);
        for (const m of matches) ids.add(m[0]);
    }
    return Array.from(ids).sort();
}

async function fetchPhoto(id, accessKey) {
    const url = `https://api.unsplash.com/photos/${encodeURIComponent(id)}`;
    const res = await fetch(url, {
        headers: { Authorization: `Client-ID ${accessKey}` },
    });
    if (!res.ok) {
        const body = await res.text().catch(() => '');
        throw new Error(`HTTP ${res.status}: ${body.slice(0, 200)}`);
    }
    const data = await res.json();
    const username = data.user?.username;
    const name = data.user?.name;
    const photoId = data.id;
    if (!username || !name || !photoId) {
        throw new Error('Missing user.username, user.name, or id in response');
    }
    return {
        photographerName: name,
        photographerUrl: `https://unsplash.com/@${username}?${UTM_QUERY}`,
        unsplashUrl: `https://unsplash.com/photos/${photoId}?${UTM_QUERY}`,
    };
}

function renderMapFile(entries) {
    const lines = [];
    lines.push('/**');
    lines.push(' * Photographer attribution for the Unsplash photos hardcoded into our');
    lines.push(' * structural templates and admin seed data.');
    lines.push(' *');
    lines.push(' * Generated by scripts/fetch-unsplash-attribution.mjs. Re-run that');
    lines.push(' * script (with UNSPLASH_ACCESS_KEY set) whenever a new photo-id is');
    lines.push(' * added to a template or seed file.');
    lines.push(' *');
    lines.push(' * A `null` entry means we have not yet fetched attribution for that');
    lines.push(' * photo. The renderer treats `null` as "no caption rendered" — but the');
    lines.push(' * surrounding code is structured so this is the only path that can');
    lines.push(' * surface an Unsplash image without a caption, making it easy to grep');
    lines.push(' * for and resolve before any production deploy.');
    lines.push(' */');
    lines.push("import type { UnsplashAttribution } from './types';");
    lines.push('');
    lines.push('export const UNSPLASH_ATTRIBUTION_MAP: Record<string, UnsplashAttribution | null> = {');
    for (const [id, attr] of entries) {
        if (attr === null) {
            lines.push(`    ${JSON.stringify(id)}: null,`);
        } else {
            lines.push(`    ${JSON.stringify(id)}: {`);
            lines.push(`        photographerName: ${JSON.stringify(attr.photographerName)},`);
            lines.push(`        photographerUrl: ${JSON.stringify(attr.photographerUrl)},`);
            lines.push(`        unsplashUrl: ${JSON.stringify(attr.unsplashUrl)},`);
            lines.push('    },');
        }
    }
    lines.push('};');
    lines.push('');
    lines.push('export function extractUnsplashPhotoId(url: string | null | undefined): string | null {');
    lines.push('    if (!url) return null;');
    lines.push('    const match = url.match(/images\\.unsplash\\.com\\/(photo-[a-zA-Z0-9_-]+)/);');
    lines.push('    return match ? match[1] : null;');
    lines.push('}');
    lines.push('');
    lines.push('export function lookupAttributionForUrl(');
    lines.push('    url: string | null | undefined,');
    lines.push('): UnsplashAttribution | null {');
    lines.push('    const id = extractUnsplashPhotoId(url);');
    lines.push('    if (!id) return null;');
    lines.push('    return UNSPLASH_ATTRIBUTION_MAP[id] ?? null;');
    lines.push('}');
    lines.push('');
    return lines.join('\n');
}

async function main() {
    const accessKey = process.env.UNSPLASH_ACCESS_KEY;
    if (!accessKey) {
        console.error('UNSPLASH_ACCESS_KEY is not set. Aborting.');
        process.exit(1);
    }

    const ids = extractIds();
    console.log(`Found ${ids.length} unique Unsplash photo-ids in templates.`);

    const entries = [];
    for (const id of ids) {
        process.stdout.write(`  ${id} ... `);
        try {
            const attr = await fetchPhoto(id, accessKey);
            entries.push([id, attr]);
            console.log(`ok (${attr.photographerName})`);
        } catch (err) {
            entries.push([id, null]);
            console.log(`FAILED — ${err.message}`);
        }
        await new Promise((resolve) => setTimeout(resolve, 1100));
    }

    const file = renderMapFile(entries);
    writeFileSync(MAP_FILE, file, 'utf8');
    console.log(`\nWrote ${MAP_FILE}`);

    const failures = entries.filter(([, a]) => a === null).length;
    if (failures > 0) {
        console.warn(
            `\n${failures} photo(s) could not be resolved. Their entries are null in the map; investigate before deploying.`,
        );
        process.exit(2);
    }
}

main().catch((err) => {
    console.error(err);
    process.exit(1);
});
